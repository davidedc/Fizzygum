// coffee-to-js-mirror.js — compile every src/**/*.coffee to a flat directory of plain-ES5
// .js files, for tools that analyze JavaScript (currently: find_similar_code.sh's
// structural-duplication scan via jsinspect; see docs/duplicated-code-detection.md).
//
// Usage:  node buildSystem/coffee-to-js-mirror.js <outDir>     (run from the Fizzygum repo root)
//
// Why CoffeeScript 1 (the `coffeescript-v1` npm-aliased devDependency), not the ^2.7.0 one:
// the sources use CS1 idioms (notably bare `super`) that the bundled IN-BROWSER compiler
// accepts but CS 2.x rejects — CS 2.7 fails ~106 of the 484 files, CS1 fails only the 6
// mixins that use `super` outside a class. Those 6 are rescued by textually replacing
// `super` with a `__SUPER__` call BEFORE compiling: semantics don't matter here, only AST
// shape, and a plain call preserves it. (Do NOT use node_modules/.bin/coffee or the alias
// package's bin/coffee: CS1's stock bin silently PREFERS a locally-installed
// node_modules/coffeescript — i.e. the 2.7.0 devDependency — so it must be bypassed via
// the compiler API, which is what this script does.)
//
// The fixed compiler-preamble helpers (extend/hasProp/bind/slice/indexOf/modulo and the
// "Generated by" header) are stripped from each output: they are byte-identical in every
// file and otherwise show up as the top "duplicate" of any downstream scan (the 2019
// jsinspect experiment had to hand-clean exactly these).
//
// Output names flatten the src/ path with "__" (basic-widgets/Widget.coffee ->
// basic-widgets__Widget.js) so the reverse mapping back to the .coffee file is lossless.
// NOTE: line numbers in the output are compiled-JS lines, NOT .coffee lines — map findings
// back via the class name (one class per file) and method name (X.prototype.method).

'use strict';

const fs = require('fs');
const path = require('path');
const cs1 = require('coffeescript-v1'); // npm alias of coffeescript@1.12.7

const outDir = process.argv[2];
if (!outDir) {
  console.error('usage: node buildSystem/coffee-to-js-mirror.js <outDir>');
  process.exit(2);
}

const srcRoot = path.join(__dirname, '..', 'src');

// CS1 emits these helper definitions verbatim at the top of any file that needs them.
const PREAMBLE_HELPERS = [
  'extend = function(child, parent) { for (var key in parent) { if (hasProp.call(parent, key)) child[key] = parent[key]; } function ctor() { this.constructor = child; } ctor.prototype = parent.prototype; child.prototype = new ctor(); child.__super__ = parent.prototype; return child; }',
  'hasProp = {}.hasOwnProperty',
  'bind = function(fn, me){ return function(){ return fn.apply(me, arguments); }; }',
  'slice = [].slice',
  'indexOf = [].indexOf || function(item) { for (var i = 0, l = this.length; i < l; i++) { if (i in this && this[i] === item) return i; } return -1; }',
  'modulo = function(a, b) { return (+a % (b = +b) + b) % b; }',
];

const walk = (dir) => fs.readdirSync(dir, { withFileTypes: true }).flatMap((e) => {
  const p = path.join(dir, e.name);
  return e.isDirectory() ? walk(p) : (e.name.endsWith('.coffee') ? [p] : []);
});

fs.rmSync(outDir, { recursive: true, force: true });
fs.mkdirSync(outDir, { recursive: true });

const failures = [];
let ok = 0;
for (const file of walk(srcRoot)) {
  const rel = path.relative(srcRoot, file);
  const shimmed = fs.readFileSync(file, 'utf8').replace(/\bsuper\b/g, '__SUPER__');
  let js;
  try {
    js = cs1.compile(shimmed, { bare: true, header: false });
  } catch (err) {
    failures.push(`${rel}: ${err.message.split('\n')[0]}`);
    continue;
  }
  js = js.replace(/^\/\/ Generated by CoffeeScript.*\n/, '');
  for (const h of PREAMBLE_HELPERS) {
    js = js.replace(',\n  ' + h, '').replace('\n  ' + h + ';', '').replace('var ' + h + ';\n', '');
  }
  fs.writeFileSync(path.join(outDir, rel.replace(/\.coffee$/, '.js').replace(/\//g, '__')), js);
  ok += 1;
}

console.log(`coffee-to-js-mirror: compiled ${ok} files to ${outDir}` +
            (failures.length ? `, FAILED ${failures.length}:` : ''));
for (const f of failures) console.error('  FAIL ' + f);
process.exit(failures.length ? 1 : 0);
